    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if("".equals(txt_qt_kg.getText()) || "".equals(txt_vl_kg.getText())){
            JOptionPane.showMessageDialog(this, "Campo vazio detectado!");
        }else{
            float qt = Float.parseFloat(txt_qt_kg.getText());
            float vl = Float.parseFloat(txt_vl_kg.getText());
            
            Material material = (Material) jComboBox1.getSelectedItem();
            int id = material.getId();
            
            ProdutoDAO dao = new ProdutoDAO();
            dao.venda(id,qt,vl);
        }

    }//GEN-LAST:event_jButton1ActionPerformed


